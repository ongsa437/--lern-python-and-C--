# 1) ‡∏™‡∏£‡πâ‡∏≤‡∏á‡∏Å‡∏£‡∏∞‡∏î‡∏≤‡∏ô XO ‡πÄ‡∏õ‡πá‡∏ô list
board = [" " for _ in range(9)]  
# " " ‡∏Ñ‡∏∑‡∏≠‡∏ä‡πà‡∏≠‡∏á‡∏ß‡πà‡∏≤‡∏á, ‡πÉ‡∏ä‡πâ 9 ‡∏ä‡πà‡∏≠‡∏á (index 0-8) ‡πÅ‡∏ó‡∏ô‡∏Å‡∏£‡∏∞‡∏î‡∏≤‡∏ô XO 3x3


# 2) ‡∏ü‡∏±‡∏á‡∏Å‡πå‡∏ä‡∏±‡∏ô‡πÅ‡∏™‡∏î‡∏á‡∏Å‡∏£‡∏∞‡∏î‡∏≤‡∏ô
def print_board():
    print("\n")
    print(f"{board[0]} | {board[1]} | {board[2]}")
    print("--+---+--")
    print(f"{board[3]} | {board[4]} | {board[5]}")
    print("--+---+--")
    print(f"{board[6]} | {board[7]} | {board[8]}")
    print("\n")


# 3) ‡∏ü‡∏±‡∏á‡∏Å‡πå‡∏ä‡∏±‡∏ô‡∏ï‡∏£‡∏ß‡∏à‡∏™‡∏≠‡∏ö‡∏ú‡∏π‡πâ‡∏ä‡∏ô‡∏∞
def check_winner(player):
    # ‡πÄ‡∏á‡∏∑‡πà‡∏≠‡∏ô‡πÑ‡∏Ç‡∏Å‡∏≤‡∏£‡∏ä‡∏ô‡∏∞ (index ‡∏Ç‡∏≠‡∏á‡∏ä‡πà‡∏≠‡∏á‡∏ö‡∏ô‡∏Å‡∏£‡∏∞‡∏î‡∏≤‡∏ô)
    win_conditions = [
        [0, 1, 2], [3, 4, 5], [6, 7, 8],   # ‡πÅ‡∏ô‡∏ß‡∏ô‡∏≠‡∏ô
        [0, 3, 6], [1, 4, 7], [2, 5, 8],   # ‡πÅ‡∏ô‡∏ß‡∏ï‡∏±‡πâ‡∏á
        [0, 4, 8], [2, 4, 6]               # ‡πÅ‡∏ô‡∏ß‡∏ó‡πÅ‡∏¢‡∏á
    ]
    for condition in win_conditions:
        if board[condition[0]] == board[condition[1]] == board[condition[2]] == player:
            return True
    return False


# 4) ‡∏ü‡∏±‡∏á‡∏Å‡πå‡∏ä‡∏±‡∏ô‡∏ï‡∏£‡∏ß‡∏à‡∏ß‡πà‡∏≤‡∏Å‡∏£‡∏∞‡∏î‡∏≤‡∏ô‡πÄ‡∏ï‡πá‡∏°‡∏´‡∏£‡∏∑‡∏≠‡πÑ‡∏°‡πà (‡πÄ‡∏™‡∏°‡∏≠)
def is_draw():
    return " " not in board


# 5) ‡πÄ‡∏£‡∏¥‡πà‡∏°‡πÄ‡∏Å‡∏°
current_player = "X"   # ‡πÄ‡∏£‡∏¥‡πà‡∏°‡∏à‡∏≤‡∏Å X ‡∏Å‡πà‡∏≠‡∏ô

while True:
    print_board()
    try:
        move = int(input(f"Player {current_player}, ‡πÄ‡∏•‡∏∑‡∏≠‡∏Å‡∏ï‡∏≥‡πÅ‡∏´‡∏ô‡πà‡∏á (1-9): ")) - 1
        # ‡∏•‡∏ö 1 ‡πÄ‡∏û‡∏£‡∏≤‡∏∞‡∏ú‡∏π‡πâ‡πÉ‡∏ä‡πâ‡πÄ‡∏•‡∏∑‡∏≠‡∏Å 1-9 ‡πÅ‡∏ï‡πà list ‡πÉ‡∏ä‡πâ index 0-8

        if board[move] != " ":
            print("‚ùå ‡∏ä‡πà‡∏≠‡∏á‡∏ô‡∏µ‡πâ‡∏ñ‡∏π‡∏Å‡πÄ‡∏•‡∏∑‡∏≠‡∏Å‡πÑ‡∏õ‡πÅ‡∏•‡πâ‡∏ß ‡∏•‡∏≠‡∏á‡πÉ‡∏´‡∏°‡πà‡∏≠‡∏µ‡∏Å‡∏Ñ‡∏£‡∏±‡πâ‡∏á")
            continue
    except (ValueError, IndexError):
        print("‚ö†Ô∏è ‡∏Å‡∏£‡∏∏‡∏ì‡∏≤‡πÉ‡∏™‡πà‡∏ï‡∏±‡∏ß‡πÄ‡∏•‡∏Ç 1-9 ‡πÄ‡∏ó‡πà‡∏≤‡∏ô‡∏±‡πâ‡∏ô")
        continue

    # ‡∏ß‡∏≤‡∏á‡∏™‡∏±‡∏ç‡∏•‡∏±‡∏Å‡∏©‡∏ì‡πå‡πÉ‡∏ô‡∏Å‡∏£‡∏∞‡∏î‡∏≤‡∏ô
    board[move] = current_player

    # ‡∏ï‡∏£‡∏ß‡∏à‡∏™‡∏≠‡∏ö‡∏ú‡∏•‡∏•‡∏±‡∏û‡∏ò‡πå
    if check_winner(current_player):
        print_board()
        print(f"üéâ ‡∏ú‡∏π‡πâ‡πÄ‡∏•‡πà‡∏ô {current_player} ‡∏ä‡∏ô‡∏∞‡πÅ‡∏•‡πâ‡∏ß!")
        break

    if is_draw():
        print_board()
        print("ü§ù ‡πÄ‡∏Å‡∏°‡πÄ‡∏™‡∏°‡∏≠!")
